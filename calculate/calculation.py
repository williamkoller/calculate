class Calculation():
    @staticmethod
    def sum(a: float, b: float) -> float:
        return a + b

    @staticmethod
    def subtraction(a: float, b: float) -> float:
        return a - b

    @staticmethod
    def division(a: float, b: float) -> float:
        return a / b

    @staticmethod
    def multiplication(a: float, b: float) -> float:
        return a * b
